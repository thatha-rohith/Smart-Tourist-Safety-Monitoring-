// @route   POST /api/efirs
router.post('/', protect, authorize('authority', 'admin'), async (req, res) => {
  try {
    const efir = await EFIR.create({
      ...req.body,
      efirNumber: generateEFIRNumber(),
      assignedOfficer: req.body.assignedOfficer || req.user._id,
    });
    const populated = await EFIR.findById(efir._id)
      .populate({ path: 'tourist', populate: { path: 'user', select: 'name email phone' } })
      .populate('assignedOfficer', 'name email badgeNumber');
    res.status(201).json(populated);
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
});

// @route   PUT /api/efirs/:id/verify
router.put('/:id/verify', protect, authorize('authority', 'admin'), async (req, res) => {
  try {
    const efir = await EFIR.findById(req.params.id);
    if (!efir) return res.status(404).json({ message: 'E-FIR not found' });

    efir.status = 'Verified';
    efir.verifiedBy = req.user._id;
    efir.verifiedAt = new Date();
    await efir.save();

    const populated = await EFIR.findById(efir._id)
      .populate({ path: 'tourist', populate: { path: 'user', select: 'name email phone' } })
      .populate('assignedOfficer', 'name email badgeNumber')
      .populate('verifiedBy', 'name email');

    res.json(populated);
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
});

// @route   PUT /api/efirs/:id
router.put('/:id', protect, authorize('authority', 'admin'), async (req, res) => {
  try {
    const efir = await EFIR.findById(req.params.id);
    if (!efir) return res.status(404).json({ message: 'E-FIR not found' });

    const fields = ['incidentType', 'severity', 'description', 'status', 'source', 'responseAction', 'assignedOfficer', 'location', 'incidentDate'];
    fields.forEach((field) => {
      if (req.body[field] !== undefined) efir[field] = req.body[field];
    });

    if (req.body.status === 'Verified' && efir.status !== 'Verified') {
      efir.verifiedBy = req.user._id;
      efir.verifiedAt = new Date();
    }

    await efir.save();
    const populated = await EFIR.findById(efir._id)
      .populate({ path: 'tourist', populate: { path: 'user', select: 'name email phone' } })
      .populate('assignedOfficer', 'name email badgeNumber')
      .populate('verifiedBy', 'name email');
    res.json(populated);
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
});

// @route   PUT /api/efirs/:id/close
router.put('/:id/close', protect, authorize('authority', 'admin'), async (req, res) => {
  try {
    const efir = await EFIR.findById(req.params.id);
    if (!efir) return res.status(404).json({ message: 'E-FIR not found' });

    efir.status = 'Closed';
    efir.responseAction = req.body.responseAction || 'Case closed by officer';
    await efir.save();

    const populated = await EFIR.findById(efir._id)
      .populate({ path: 'tourist', populate: { path: 'user', select: 'name email phone' } })
      .populate('assignedOfficer', 'name email badgeNumber');
    res.json(populated);
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
});

// @route   DELETE /api/efirs/:id
router.delete('/:id', protect, authorize('authority', 'admin'), async (req, res) => {
  try {
    const efir = await EFIR.findById(req.params.id);
    if (!efir) return res.status(404).json({ message: 'E-FIR not found' });
    if (efir.status === 'Verified') {
      return res.status(400).json({ message: 'Cannot delete verified E-FIR records' });
    }
    await efir.deleteOne();
    res.json({ message: 'E-FIR deleted successfully' });
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
});

module.exports = router;
