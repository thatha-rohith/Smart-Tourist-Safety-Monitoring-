const express = require('express');
const EFIR = require('../models/EFIR');
const { protect, authorize } = require('../middleware/auth');

const router = express.Router();

const generateEFIRNumber = () => {
  const year = new Date().getFullYear();
  const random = Math.floor(1000 + Math.random() * 9000);
  return `EFIR/${year}/${random}`;
};

// @route   GET /api/efirs
router.get('/', protect, authorize('authority', 'admin'), async (req, res) => {
  try {
    const { search, status, incidentType } = req.query;
    let query = {};

    if (status) query.status = status;
    if (incidentType) query.incidentType = incidentType;

    let efirs = await EFIR.find(query)
      .populate({ path: 'tourist', populate: { path: 'user', select: 'name email phone' } })
      .populate('assignedOfficer', 'name email badgeNumber')
      .populate('verifiedBy', 'name email')
      .sort({ createdAt: -1 });

    if (search) {
      const s = search.toLowerCase();
      efirs = efirs.filter(
        (e) =>
          e.efirNumber.toLowerCase().includes(s) ||
          e.tourist?.user?.name?.toLowerCase().includes(s) ||
          e.incidentType.toLowerCase().includes(s)
      );
    }

    res.json(efirs);
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
});

// @route   GET /api/efirs/stats
router.get('/stats', protect, authorize('authority', 'admin'), async (req, res) => {
  try {
    const total = await EFIR.countDocuments();
    const verified = await EFIR.countDocuments({ status: 'Verified' });
    const pending = await EFIR.countDocuments({ status: 'Pending' });
    const startOfMonth = new Date();
    startOfMonth.setDate(1);
    startOfMonth.setHours(0, 0, 0, 0);
    const thisMonth = await EFIR.countDocuments({ createdAt: { $gte: startOfMonth } });
    res.json({ total, verified, pending, thisMonth });
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
});

// @route   GET /api/efirs/:id
router.get('/:id', protect, authorize('authority', 'admin'), async (req, res) => {
  try {
    const efir = await EFIR.findById(req.params.id)
      .populate({ path: 'tourist', populate: { path: 'user', select: 'name email phone' } })
      .populate('assignedOfficer', 'name email badgeNumber department')
      .populate('verifiedBy', 'name email')
      .populate('alert');
    if (!efir) return res.status(404).json({ message: 'E-FIR not found' });
    res.json(efir);
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
});

